// 3. Simple math
// JavaScript supports basic arithmetic operations: +, -, *, /
let sum = 5 + 3; // Addition
let product = 4 * 2; // Multiplication
let difference = 10 - 7; // Subtraction
let quotient = 20 / 4; // Division
let remainder = 10 % 3; // Modulus (remainder)
let exponent = 2 ** 3; // Exponentiation (2 to the power of 3)

console.log("5 + 3 =", sum); // 8
console.log("4 * 2 =", product); // 8
console.log("10 - 7 =", difference); // 3
console.log("20 / 4 =", quotient); // 5
console.log("10 % 3 =", remainder); // 1
console.log("2 ** 3 =", exponent); // 8

// You can also use math functions from the Math object:
console.log("Math.sqrt(16) =", Math.sqrt(16)); // 4
console.log("Math.max(1, 5, 3) =", Math.max(1, 5, 3)); // 5
