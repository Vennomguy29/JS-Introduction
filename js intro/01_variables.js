// 1. Variables: store data
// Variables are containers for storing data values.
// 'let' allows you to declare a variable that can be changed later.
let name = "Alice"; // You can change 'name' later
// 'const' creates a constant variable that cannot be reassigned.
const country = "USA"; // This value cannot be changed
// Variables can store different types of data, such as numbers, strings, or booleans.
let age = 25; // Number type
// You can also declare multiple variables at once:
let city = "New York", isStudent = false;
// Output the variables
console.log("Name:", name);
console.log("Country:", country);
console.log("Age:", age);
console.log("City:", city);
console.log("Is student?", isStudent);
