// 6. Loops: repeat actions
// Loops let you repeat code multiple times.
// The 'for' loop is common for counting:
for (let i = 1; i <= 3; i++) {
    console.log("Count: " + i);
}

// The 'while' loop repeats as long as a condition is true:
let n = 1;
while (n <= 3) {
    console.log("While loop count:", n);
    n++;
}

// The 'do...while' loop always runs at least once:
let x = 1;
do {
    console.log("Do...while count:", x);
    x++;
} while (x <= 3);
