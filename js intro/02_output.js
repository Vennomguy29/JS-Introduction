// 2. Output: show messages
// You can display output in JavaScript using console.log().
// This is useful for debugging and seeing results in the console.
console.log("Hello, " + name + "!"); // Concatenation with +
console.log("You are " + age + " years old.");
console.log("You live in " + country + ".");
// You can also use commas to separate values:
console.log("Name:", name, "Country:", country);
// Template literals (see section 11) are another way to output variables:
console.log(`Welcome, ${name} from ${country}!`);
